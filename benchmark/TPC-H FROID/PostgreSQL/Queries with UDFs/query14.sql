SELECT 
    100.00 * SUM(PROMO_DISC(P_TYPE, L_EXTENDEDPRICE, L_DISCOUNT)) / SUM(DISCOUNT_PRICE(L_EXTENDEDPRICE, L_DISCOUNT)) AS PROMO_REVENUE
FROM 
    LINEITEM, 
    PART
WHERE 
    L_PARTKEY = P_PARTKEY 
    AND L_SHIPDATE >= DATE '1995-09-01' 
    AND L_SHIPDATE < DATE '1995-09-01' + INTERVAL '1 MONTH';