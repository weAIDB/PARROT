SELECT 
    C_CUSTKEY, 
    C_NAME, 
    SUM(DISCOUNT_PRICE(L_EXTENDEDPRICE, L_DISCOUNT)) AS REVENUE, 
    C_ACCTBAL, 
    N_NAME, 
    C_ADDRESS, 
    C_PHONE, 
    C_COMMENT
FROM 
    CUSTOMER, 
    ORDERS, 
    LINEITEM, 
    NATION
WHERE 
    C_CUSTKEY = O_CUSTKEY 
    AND L_ORDERKEY = O_ORDERKEY 
    AND Q10CONDITIONS(O_ORDERDATE, L_RETURNFLAG) = 1 
    AND C_NATIONKEY = N_NATIONKEY
GROUP BY 
    C_CUSTKEY, 
    C_NAME, 
    C_ACCTBAL, 
    C_PHONE, 
    N_NAME, 
    C_ADDRESS, 
    C_COMMENT
ORDER BY 
    REVENUE DESC NULLS LAST
LIMIT 
    20;